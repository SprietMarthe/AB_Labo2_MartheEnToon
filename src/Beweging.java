public class Beweging {

    int tijd;
    Positie p1, p2;
    int snelheid;

    public int getTijd() {
        return tijd;
    }

    public void setTijd(int tijd) {
        this.tijd = tijd;
    }

    public Positie getP1() {
        return p1;
    }

    public void setP1(Positie p1) {
        this.p1 = p1;
    }

    public Positie getP2() {
        return p2;
    }

    public void setP2(Positie p2) {
        this.p2 = p2;
    }

    public int getSnelheid() {
        return snelheid;
    }

    public void setSnelheid(int snelheid) {
        this.snelheid = snelheid;
    }
}
